    private void loadDataToTable() {
        model.setRowCount(0);
        listSanPham = sanPhamService.getAllResponse();
        int index = 1;
        for (SanPhamResponse xx : listSanPham) {
            model.addRow(xx.toDataRow(index));
        }
    }

    private SanPham getFormData() {
        SanPham sanPham = new SanPham();
        sanPham.setMa(txtMa.getText().trim());
        sanPham.setTen(txtTen.getText().trim());
        return sanPham;
    }

