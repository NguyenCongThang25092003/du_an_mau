    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            String message = chucVuService.insert(getFormData());
            loadDataToTable();
            JOptionPane.showMessageDialog(this, message);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        try {
            ChucVu chucVu = getFormData();
            chucVu.setId(listChucVu.get(tblChucVu.getSelectedRow()).getId());
            String message = chucVuService.update(chucVu);
            JOptionPane.showMessageDialog(this, message);
            loadDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            ChucVuResponse chucVuResponse = listChucVu.get(tblChucVu.getSelectedRow());
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không?");
            if (hoi != JOptionPane.YES_OPTION) {
                return;
            }
            String message = chucVuService.delete(chucVuResponse.getId());
            loadDataToTable();
            JOptionPane.showMessageDialog(this, message);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private ChucVu getFormData() {
        ChucVu chucVu = new ChucVu();
        chucVu.setMa(txtMa.getText().trim());
        chucVu.setTen(txtTen.getText().trim());
        return chucVu;
    }

