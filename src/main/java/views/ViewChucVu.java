    private void loadDataToTable() {
        model.setRowCount(0);
        int index = 1;
        listChucVu = chucVuService.getAllResponse();
        for (ChucVuResponse xx : listChucVu) {
            model.addRow(xx.toDataRow(index));
            index++;
        }
    }

